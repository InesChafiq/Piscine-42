int		ft_str_is_lowercase(char *str)
{
	int		i;

	i = -1;
	if (!str[0])
	{
		return (1);
	}
	while (str[++i])
	{
		if (str[i] > 'z' || str[i] < 'a')
		{
			return (0);
		}
	}
	return (1);
}
