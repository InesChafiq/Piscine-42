#include <stdio.h>

int		ft_sqrt(int nb)
{
	int		i;

	i = 0;
	if (nb == 1)
		return (1);
	if (nb < 0)
		nb *= -1;
	while (i < nb / i)
	{
		if ((i * i == nb) && (nb % i == 0))
			return (i);
		i++;
	}
	return (0);
}

int		main(void)
{
	printf("%d",ft_sqrt(2147395600));
	return (0);
}
