int		ft_iterative_factorial(int nb)
{
	int		i;

	i = 1;
	if (nb == 0)
		return (1);
	if (nb < 0)
		return (0);
	while (0 < nb)
	{
		i = i * nb;
		nb--;
	}
	return (i);
}
